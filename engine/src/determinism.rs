//! Cross-target determinism test support.
//!
//! This module is **test-only** (`#[cfg(test)]`). It exposes:
//!
//! - `EXPECTED_FINGERPRINT`: the pinned byte sequence that must be
//!   identical on both `wasm32-unknown-unknown` and native `x86_64`.
//! - `compute_determinism_fingerprint()`: exercises every public
//!   arithmetic and serialization path and returns the byte sequence.
//! - `print_and_panic_on_empty()`: first-run helper that prints the
//!   new fingerprint bytes when the constant is empty.
//!
//! Both the native integration test (`engine/tests/determinism.rs`)
//! and the wasm-bindgen-test (`engine/tests/determinism_wasm.rs`)
//! import from here so they call the **exact same code** and compare
//! against the **exact same constant**. Any divergence between
//! targets is a real determinism bug, not a copy-paste drift.
//!
//! # Why this lives in the library
//!
//! `wasm-bindgen-test` instruments `#[wasm_bindgen_test]` functions
//! compiled into the wasm target. Integration test files under
//! `engine/tests/` are separate crates that cannot be targeted by
//! `wasm-pack test`. Moving the computation into the library (behind
//! `#[cfg(test)]`) lets both test harnesses reference a single source
//! of truth. See ADR-0003 and BACKLOG.md Atom B for context.

